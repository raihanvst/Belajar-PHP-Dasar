<?php

    $a = 60;
    $b = 13;

    //Bitwise AND
    $c = $a & $b;
    echo "$a & $b = $c";
    echo "<br>";

    //Bitwise OR
    $c = $a | $b;
    echo "$a | $b = $c";
    echo "<br>";

    //Bitwise XOR
    $c = $a ^ $b;
    echo "$a ^ $b = $c";
    echo "<br>";

    //Shift Left
    $c = $a << $b;
    echo "$a << $b = $c";
    echo "<br>";

    //Shift Right
    $c = $a >> $b;
    echo "$a >> $b = $c";
    echo "<br>";
?>